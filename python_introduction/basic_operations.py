#!/usr/bin/python

number1 = 10

number2 = 5

sum_1 = (number1 + number2)

min_1 = (number1 - number2)
mult = (number1 * number2)
print("Addition of {0:d} and {1:d} is {2:d}".format(number1, number2, sum_1))

print("Subtraction of {0:d} and {1:d} is {2:d}".format(number1, number2, min_1))

print("Multiplication of {0:d} and {1:d} is {2:d}".format(number1, number2, mult))
